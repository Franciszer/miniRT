#include "minirt.h"

t_vec3	cross_vec3(t_vec3 v, t_vec3 w)
{
	t_vec3	z;

	z.x = v.y * w.z - v.z * w.y;
	z.y = v.z * w.x - v.x * w.z;
	z.z = v.x * w.y - v.y * w.x;
	return (z);
}
