#include "minirt.h"

double	norm(t_vec3 v)
{
	return (sqrt(pow(v.x, 2) + pow(v.y, 2) + pow(v.z, 2)));
}

double	norm2(t_vec3 v)
{
	return (pow(norm(v), 2));
}
