#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t num)
{
	size_t			i;
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	i = 0;
	if (s < d)
		while (++i <= num)
			d[num - i] = s[num - i];
	else
		while (num-- > 0)
			*(d++) = *(s++);
	return (dest);
}
