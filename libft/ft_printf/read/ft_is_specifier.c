#include "../ft_printf.h"

int		ft_is_specifier(char c)
{
	if (c == 'd' || c == 'i' || c == 'u' || c == 'x' || c == 'X' ||\
		c == 'c' || c == 's' || c == 'p' || c == '%')
		return (1);
	return (0);
}
